csv_data = pd.read_csv("real_estate_dataset.csv")
json_data = pd.read_json("real_estate_dataset.json")

print(csv_data.head())
print(json_data.head())

csv_data = {
    "Location": ["NIMS Area", "Abu Road", "Mount Abu"],
    "Distance": [2, 10, 20],
    "Price": [500000, 200000, 150000]
}

df_csv = pd.DataFrame(csv_data)

