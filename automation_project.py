json_data = {
    "Location": ["NIMS Area", "Outside NIMS"],
    "Distance": [1, 15],
    "Price": [700000, 120000]
}

df_json = pd.DataFrame(json_data)

df_json.to_json("real_estate_dataset.json")

print("JSON File Created")

csv_file = pd.read_csv("real_estate_dataset.csv")

json_file = pd.read_json("real_estate_dataset.json")

print(csv_file)
print(json_file)

merged_data = pd.concat([csv_file, json_file, ])

print(merged_data)

merged_data.dropna(inplace=True)

merged_data.drop_duplicates(inplace=True)

print(merged_data)

def area_type(distance):
    if distance <= 5:
        return "Near NIMS"
    else:
        return "Outside NIMS"

merged_data["Area_Type"] = merged_data["Distance"].apply(area_type)

print(merged_data)

def price_category(distance):
    if distance <= 5:
        return "High Price"
    else:
        return "Low Price"

merged_data["Price_Category"] = merged_data["Distance"].apply(price_category)

print(merged_data)

merged_data.to_csv("final_land_data.csv", index=False)

print("Final Dataset Saved")

plt.figure(figsize=(8,5))

plt.scatter(merged_data["Distance"], merged_data["Price"])

plt.xlabel("Distance From NIMS")

plt.ylabel("Land Price")

plt.title("Distance vs Land Price")

plt.show()

X = merged_data[["Distance"]]

y = merged_data["Price"]

model = LinearRegression()

model.fit(X, y)

predicted_price = model.predict([[7]])

print("Predicted Price:", predicted_price)

